print('Введите дату рождения: ')

d = int(input('День: '))
m = int(input('Месяц: '))
year = int(input('Год: '))

if m < 3:
    year -= 1
    m += 10
else:
    m -= 2

y = year % 100
c = year // 100

dow = d + ((13 * m - 1) // 5) + y + (y // 4 + c // 4 - 2 * c + 777)
week = ['Воскресенье', 'Понедельник', 'Вторник', 'Среда', 'Четверг', 'Пятница', 'Суббота']
print(week[dow % 7])
